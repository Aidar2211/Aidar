a = float(input("введите число"))
b = float(input("введите число"))
c = float(input("введите число"))

data = input("введите значение +, -, /, *")

if data == "+":
    d=a+b+c
    print("результат" + str(d))

if data == "-":
     d=a-b-c
     print("результат" + str(d))

if data == "/":
     d=a/b/c
     print("результат" + str(d))

if data == "*":
     d=a*b*c
     print("результат" + str(d))



